    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // Grava os dados da academia
        Academia academia = new Academia();
        DaoAcademia dao = new DaoAcademia();
        BotaoAcaoAv bt = new BotaoAcaoAv(this);
        if (jTid_academia.getText().equals("")) {
            academia.setid_academia(Integer.valueOf(jTid_academia.getText()));            
            academia.setnome(jTnome.getText());
            academia.setendereco(jTendereco.getText());
            academia.setbairro(jTbairro.getText());
            academia.setcidade(jTcidade.getText());
            academia.setestado(jTestado.getText());
            academia.setcep(jTcep.getText());
            academia.settelefone(jTtelefone.getText());
            academia.setemail(jTemail.getText());
            academia.setsite(jTsite.getText());
            academia.setcontato(jTcontato.getText());             
            
            try {
                dao.incluir(academia);
                JOptionPane.showMessageDialog(null, "Academia cadastrada com sucesso!", "Confirmação!", 1);
                bt.LimpaCampos();
                bt.setNavegacao();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao gravar dados!", "Atenção!", 1);
                System.out.println("Gravacao de Academia " + ex);
                bt.LimpaCampos();
                bt.setNavegacao();
                Logger.getLogger(TelaAcademia.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            System.out.println("passou aqui");
            AlteraAcademia();
        }
    }                                        


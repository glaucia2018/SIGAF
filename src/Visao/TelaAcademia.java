    public void ListarAcademia() {
        DaoAcademia dao = new DaoAcademia();
        Academia usuario = new Academia();

        String pesquisa = "%" + jTPesquisa.getText() + "%";
        usuario.setNome(pesquisa);
        try {
            lista = dao.pesquisar(usuario);
            MostraPesquisa(lista);
        } catch (SQLException ex) {
            Logger.getLogger(TelaUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void MostraPesquisa(List<Academia> Academia) {
        while (tmAcademia.getRowCount() > 0) {
            tmAcademia.removeRow(0);
        }
        if (!Academia.isEmpty()) {
            String[] linha = new String[]{null, null};
            for (int i = 0; i < Academia.size(); i++) {
                tmAcademia.addRow(linha);
                tmAcademia.setValueAt(Academia.get(i).getNome(), i, 0);
                tmAcademia.setValueAt(Academia.get(i).getEmail(), i, 1);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Academia não cadastrada!", "Atenção!", 0);
        }

    }
    
       private void selecionarLinhaTabela(int posicao) {
        int quantidadeLinhas = this.jTTabela.getRowCount();
        if ((posicao >= 0) && (posicao < quantidadeLinhas)) {
           this.jTTabela.requestFocus();
           this.jTTabela.clearSelection();
            this.jTTabela.addRowSelectionInterval(posicao, posicao);
        }
     }

